#hier wordt string slicing behandled

zin = "Liesje leerde lotje lopen in de lange linde laan"
print(len(zin))     #laat de lengte van de zin zien

print(zin[:4])      #Laat de eerste 4 symbolen zien

print(zin[4:])      #sla de eerste 4 symbolen over

print(zin[::-1])    #Laat de string achterstevoren zien
